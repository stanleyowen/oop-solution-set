#include <iostream>

int main() {
	// Print the following pattern to the terminal
	std::cout << "       *\n";
	std::cout << "      *\n";
	std::cout << "     *\n";
	std::cout << "*   *\n";
	std::cout << " * *\n";
	std::cout << "  *\n";

	return 0;
}
